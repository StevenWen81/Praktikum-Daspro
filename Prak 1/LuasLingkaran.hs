module LuasLingkaran where

-- DEFINISI DAN SPESIFIKASI
luasLingkaran :: Float -> Float
-- menghitung luas lingkaran yang berjari-jari r

-- REALISASI
luasLingkaran r = 3.1415 * r * r -- asumsi r>0

